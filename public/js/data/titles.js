export function getTitleName(pet) {
  if (!pet || !pet.titleId) return "";
  if (pet.titleId === 5 && pet.passive) {
    const legend = legendaryTitles[pet.passive] ?? legendaryTitles[normalPassiveOf(pet.passive)];
    if (legend) return legend.name;
  }
  if (pet.isBoss && pet.titleGroup) {
    const group = bossTitles[pet.titleGroup] ?? [];
    return group.find((t) => t.id === pet.titleId)?.name ?? "";
  }
  for (const group of Object.values(enemyTitles)) {
    const found = group.find((t) => t.id === pet.titleId);
    if (found) return found.name;
  }
  return "";
}

export function normalPassiveOf(legendaryPassive) {
  for (const [key, val] of Object.entries(legendaryTitles)) {
    if (val.legendaryPassive === legendaryPassive) return key;
  }
  return null;
}

export function isLegendaryPassive(passive) {
  return Object.values(legendaryTitles).some((t) => t.legendaryPassive === passive);
}
