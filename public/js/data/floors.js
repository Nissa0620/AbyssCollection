// フロア番号から該当フロア帯キーを取得
export function getCurrentAreaKey(floor) {
  return Object.entries(floorTable).find(
    ([, band]) => band.min != null && floor >= band.min && floor <= band.max
  )?.[0] ?? null;
}

// フロア番号から該当フロア帯を取得（通常フロア用）
export function getCurrentArea(floor) {
  return Object.values(floorTable).find(
    (band) => band.min != null && floor >= band.min && floor <= band.max
  ) ?? null;
}
